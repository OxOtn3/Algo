# 多重背包

# 有 N 件物品和一个容量是 C 的背包。
# 第 i 件物品的体积是 weight[i]，价值是 val[i]，物品件数为cnt[i]。
# 求解将哪些物品装入背包，可使这些物品的总体积不超过背包容量，且总价值最大。

# 时间复杂度：O(N * C * S)
# 空间复杂度：O(N * C)
